package org.example;

public class Operations {
    public static int plus(int n1, int n2) {
        return n1 + n2;
    }

    public static int minus(int n1, int n2) {
        return n1 - n2;
    }

    public static int mul(int n1, int n2) {
        return n1 * n2;
    }

    public static int div(int n1, int n2) {
        return n1 / n2;
    }
}
